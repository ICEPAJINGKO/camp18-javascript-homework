let facts = { numPlanets: 8, yearNeptuneDiscovered: 1846 };
let { numPlanets, yearNeptuneDiscovered } = facts;

console.log(numPlanets); // 8 เพราะอยู่ตำแหน่งที่ 0 เดียวกับ facts.numPlanets
console.log(yearNeptuneDiscovered); // 1846 เพราะอยู่ตำแหน่งที่ 1 เดียวกับ facts.yearNeptuneDiscovered

